    private void AddTotalSolveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddTotalSolveButtonActionPerformed
        String dateOfSolving = SolvedateField.getText();
        String solvedProblems = AddTotalSolveField.getText();
        int totalProblems = 0;
        try{
            totalProblems = Integer.parseInt(solvedProblems);;
            TotalSolve solve = new TotalSolve(totalProblems);
            int target = solve.readTarget();
            try{
                System.out.println(totalProblems/target);
                solve.saveTotalSolveOverAll();
                Calculation progressOfTheDay = new Calculation(target,totalProblems,dateOfSolving);
                double percentage = progressOfTheDay.findAndSaveProgress();
                ResultOfToday.setText("Date: "+dateOfSolving+"\n"+"Total Solved: "+String.valueOf(totalProblems)+"\n"+"You have achieved "+String.valueOf(percentage)+"% of your goal!"+"\n");
                SolvedateField.setText(null);
                AddTotalSolveField.setText(null);
                JOptionPane.showMessageDialog(this, "Good Job Coder! See you tomorrow!");
            }
            catch(ArithmeticException e){
                JOptionPane.showMessageDialog(this, "Cannot take you further if you try to take a shortcut!\nYou cannot proceed with 0 target");
            }
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Invalid Input!");
        }
    }//GEN-LAST:event_AddTotalSolveButtonActionPerformed

