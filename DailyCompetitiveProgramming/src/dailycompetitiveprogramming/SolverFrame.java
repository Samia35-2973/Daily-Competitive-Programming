    private void getfeedbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_getfeedbuttonActionPerformed
        String rangePercentage = FeedbackComboBox.getItemAt(FeedbackComboBox.getSelectedIndex());
        if(rangePercentage.equals("0%")) JOptionPane.showMessageDialog(this, "You are not dedicated yet. CP world is a world of excitement.\nThere are coders like you rock everyday.\nPlease Start with me");
        else if(rangePercentage.equals("1-30%")) JOptionPane.showMessageDialog(this, "You are about to start competitive programming. Let's Go!");
        else if(rangePercentage.equals("31-60%")) JOptionPane.showMessageDialog(this, "You have to keep up with this streak.\nYou can do it!");
        else if(rangePercentage.equals("61-99%")) JOptionPane.showMessageDialog(this, "Just a little more hardwork will take\nyou to the world of competitive programming");
        else if(rangePercentage.equals("100%")) JOptionPane.showMessageDialog(this, "You just took off towards the competitive\nprogramming world. Good luck with your contest.\nSee you again tomorrow");
    }//GEN-LAST:event_getfeedbuttonActionPerformed

